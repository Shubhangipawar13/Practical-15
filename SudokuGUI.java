    private void StartGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_StartGameActionPerformed
        // TODO add your handling code here:
         jTextField3.removeAll();
        
        String InputGridSize = jTextField1.getText();
         gridSize =  Integer.parseInt(InputGridSize);
       // int difficultyLevel = difficultyComboBox.getSelectedIndex() + 1;
        SudokuHelper game = new SudokuHelper(gridSize,Integer.parseInt(difficultyComboBox.getItemAt(difficultyComboBox.getSelectedIndex()).charAt(6)+""));
           int[][] arr = game.board;
        jPanel1.setLayout(new GridLayout(gridSize, gridSize));
        jPanel1.removeAll();
        
        textField = new JTextField[gridSize][gridSize];
         
        
        
        for(int i=0; i<gridSize; i++){
            for(int j=0; j<gridSize; j++){
                textField[i][j] = new JTextField(2);
                jPanel1.add(textField[i][j]);
            }
        }
        for(int i=0;i<gridSize;i++){
            for(int j=0;j<gridSize;j++){
                if(arr[i][j]!=0){
                textField[i][j].setText(arr[i][j]+"");
                textField[i][j].setEditable(false);
            }    }
        }
        jPanel1.repaint();
         jPanel1.validate(); 
       
//        JoptionPane.showMessageDialog(null, "Please enter a valid Grid");
        
    }//GEN-LAST:event_StartGameActionPerformed

