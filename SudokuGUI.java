    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int count = 1;
          for(int i=0; i<gridSize;i++){
              for(int j=0;j<gridSize;j++){
                  if(isUsedBefore(i,j,textField[i][j])){
                    jTextField3.setText("Incorrect answer. Try again!");
                    return;
                  }else{
                      count++;
                  }
              }
          }
          if(count==gridSize){
          jTextField3.setText("Congratulations!"); 
          }
    }//GEN-LAST:event_jButton1ActionPerformed

    private boolean isUsedBefore(int row,int col,JTextField field){
         for(int i=0;i<gridSize;i++){
            if(textField[row][i].getText().trim().equals(field.getText().trim())){
                return true;
            }
        }
        
        for(int i=0;i<gridSize;i++){
            if(textField[i][col].getText().trim().equals(field.getText().trim())){
                return true;
            }
        }
        
       
      return false;  
    }
